import UIKit

extension UIColor {
    
    static func mainWhite() -> UIColor {
        return #colorLiteral(red: 0.9273874164, green: 0.9465356469, blue: 0.9670229554, alpha: 1)
    }
    
    static func mainBlue() -> UIColor {
        return #colorLiteral(red: 0.2828595638, green: 0.6165678501, blue: 0.9000142217, alpha: 1)
    }
    
    static func darkBlue() -> UIColor {
        return #colorLiteral(red: 0.2303113937, green: 0.4995339513, blue: 0.7292042375, alpha: 1)
    }
}
