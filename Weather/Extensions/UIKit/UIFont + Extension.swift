import UIKit

extension UIFont {
    
    static func helveticaNeueLight22() -> UIFont? {
        return UIFont.init(name: "HelveticaNeue-Light", size: 22)
    }
    
    static func helveticaNeueLight26() -> UIFont? {
        return UIFont.init(name: "HelveticaNeue-Light", size: 26)
    }
    
    static func helveticaNeueLight30() -> UIFont? {
        return UIFont.init(name: "HelveticaNeue-Light", size: 30)
    }
}
